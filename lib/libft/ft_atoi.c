#include "libft.h"

int	ft_atoi(const char *s)
{
	int	i;
	int	f;
	int	res;

	i = 0;
	f = 1;
	while ((s[i] == 32) || (9 <= s[i] && s[i] <= 13))
		i++;
	if (s[i] == '-')
	{
		f = -1;
		i++;
	}
	else if (s[i] == '+')
		i++;
	res = 0;
	while (s[i] && '0' <= s[i] && s[i] <= '9')
	{
		res *= 10;
		res += s[i] - '0';
		i++;
	}
	return (res * f);
}
